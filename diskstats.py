def build_report() -> Report:
    rep = Report(
        hostname=platform.node() or "localhost",
        system=platform.system(),
        release=platform.release(),
    )
    try:
        rep.filesystems = collect_filesystems()
    except Exception as exc:  # pragma: no cover — defensive
        rep.notes.append(f"filesystem collection failed: {exc}")
    try:
        rep.drives = collect_drives()
    except Exception as exc:  # pragma: no cover — defensive
        rep.notes.append(f"drive collection failed: {exc}")
    try:
        rep.zpools = collect_zpools()
    except Exception as exc:  # pragma: no cover — defensive
        rep.notes.append(f"zpool collection failed: {exc}")
    return rep


