def _esc(s: str) -> str:
    return (s.replace("&", "&amp;")
             .replace("<", "&lt;")
             .replace(">", "&gt;"))


def _html_bar(pct: float) -> str:
    pct = max(0.0, min(100.0, pct))
    if pct >= USAGE_CRIT:
        color = "#e06c75"
    elif pct >= USAGE_WARN:
        color = "#e5c07b"
    else:
        color = "#98c379"
    return (
        f'<div style="width:180px;height:10px;background:#2c313a;'
        f'border-radius:4px;overflow:hidden;display:inline-block;'
        f'vertical-align:middle;">'
        f'<div style="width:{pct:.1f}%;height:100%;background:{color};"></div>'
        f'</div>'
    )


def render_html(report: Report) -> str:
    font = ("-apple-system,BlinkMacSystemFont,'Segoe UI',Roboto,Helvetica,"
            "Arial,sans-serif")
    mono = "ui-monospace,'SF Mono',Menlo,Consolas,monospace"
    css_table = ('width:100%;border-collapse:collapse;font-size:13px;'
                 'font-family:' + mono + ';')
    css_th = ('text-align:left;padding:6px 10px;border-bottom:1px solid #3a3f4b;'
              'color:#c8ccd4;font-weight:600;background:#242830;')
    css_td = 'padding:6px 10px;border-bottom:1px solid #2c313a;color:#d7dae0;'
    css_td_r = css_td + 'text-align:right;'

    parts: List[str] = []
    parts.append(
        '<div style="background:#1e222a;color:#d7dae0;padding:24px;'
        f'font-family:{font};max-width:820px;border-radius:8px;">'
    )
    parts.append(
        f'<div style="font-size:20px;font-weight:600;color:#61afef;">'
        f'Storage Report &mdash; {_esc(report.hostname)}</div>'
        f'<div style="font-size:13px;color:#8a8f99;margin-bottom:18px;">'
        f'{_esc(report.system)} {_esc(report.release)}</div>'
    )

    # Filesystems
    parts.append('<h3 style="color:#61afef;margin:16px 0 8px;font-size:15px;">'
                 'Filesystems</h3>')
    parts.append(f'<table style="{css_table}">')
    parts.append(
        f'<tr><th style="{css_th}">Mount</th><th style="{css_th}">Type</th>'
        f'<th style="{css_th};text-align:right;">Size</th>'
        f'<th style="{css_th};text-align:right;">Used</th>'
        f'<th style="{css_th};text-align:right;">Free</th>'
        f'<th style="{css_th};text-align:right;">Use%</th>'
        f'<th style="{css_th}">Usage</th></tr>'
    )
    for fs in report.filesystems:
        parts.append(
            '<tr>'
            f'<td style="{css_td}"><div>{_esc(fs.mount)}</div>'
            f'<div style="font-size:11px;color:#8a8f99;">{_esc(fs.source)}</div></td>'
            f'<td style="{css_td}">{_esc(fs.fstype)}</td>'
            f'<td style="{css_td_r}">{_esc(human_bytes(fs.size))}</td>'
            f'<td style="{css_td_r}">{_esc(human_bytes(fs.used))}</td>'
            f'<td style="{css_td_r}">{_esc(human_bytes(fs.avail))}</td>'
            f'<td style="{css_td_r}">{fs.percent:.1f}%</td>'
            f'<td style="{css_td}">{_html_bar(fs.percent)}</td>'
            '</tr>'
        )
    parts.append('</table>')

    # Drives
    parts.append('<h3 style="color:#61afef;margin:20px 0 8px;font-size:15px;">'
                 'Physical Drives</h3>')
    parts.append(f'<table style="{css_table}">')
    parts.append(
        f'<tr><th style="{css_th}">Device</th><th style="{css_th}">Kind</th>'
        f'<th style="{css_th};text-align:right;">Size</th>'
        f'<th style="{css_th}">Model</th></tr>'
    )
    if not report.drives:
        parts.append(f'<tr><td style="{css_td}" colspan="4">'
                     '<em>(none detected)</em></td></tr>')
    for d in report.drives:
        parts.append(
            '<tr>'
            f'<td style="{css_td}">{_esc(d.name)}</td>'
            f'<td style="{css_td}">{_esc(d.kind or "—")}</td>'
            f'<td style="{css_td_r}">{_esc(human_bytes(d.size))}</td>'
            f'<td style="{css_td};color:#8a8f99;">{_esc(d.model or "—")}</td>'
            '</tr>'
        )
    parts.append('</table>')

    # ZFS
    parts.append('<h3 style="color:#61afef;margin:20px 0 8px;font-size:15px;">'
                 'ZFS Pools</h3>')
    if not report.zpools:
        parts.append('<div style="color:#8a8f99;font-size:13px;">'
                     '<em>(no pools)</em></div>')
    else:
        parts.append(f'<table style="{css_table}">')
        parts.append(
            f'<tr><th style="{css_th}">Pool</th><th style="{css_th}">Health</th>'
            f'<th style="{css_th};text-align:right;">Size</th>'
            f'<th style="{css_th};text-align:right;">Alloc</th>'
            f'<th style="{css_th};text-align:right;">Free</th>'
            f'<th style="{css_th}">Usage</th></tr>'
        )
        for p in report.zpools:
            pct_val = (p.alloc / p.size * 100.0) if (p.size and p.alloc is not None) else 0.0
            h = (p.health or "").upper()
            hcolor = "#98c379" if h == "ONLINE" else (
                "#e5c07b" if h in ("DEGRADED", "OFFLINE", "REMOVED") else "#e06c75"
            )
            parts.append(
                '<tr>'
                f'<td style="{css_td}">{_esc(p.name)}</td>'
                f'<td style="{css_td};color:{hcolor};font-weight:600;">{_esc(p.health)}</td>'
                f'<td style="{css_td_r}">{_esc(human_bytes(p.size))}</td>'
                f'<td style="{css_td_r}">{_esc(human_bytes(p.alloc))}</td>'
                f'<td style="{css_td_r}">{_esc(human_bytes(p.free))}</td>'
                f'<td style="{css_td}">{_html_bar(pct_val)} '
                f'<span style="color:#8a8f99;font-size:12px;">{pct_val:.1f}%</span></td>'
                '</tr>'
            )
        parts.append('</table>')

    parts.append(
        '<div style="margin-top:20px;font-size:11px;color:#6b6f79;">'
        f'diskstats {SCRIPT_VERSION} &middot; generated on '
        f'{_esc(report.hostname)}</div>'
    )
    parts.append('</div>')
    return "".join(parts)


