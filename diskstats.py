def collect_drives_linux() -> List[Drive]:
    if not which("lsblk"):
        return []
    rc, out, _ = run([
        "lsblk", "-dn",
        "-o", "NAME,SIZE,MODEL,ROTA,TRAN,TYPE",
        "--bytes",
    ])
    if rc != 0:
        return []
    drives: List[Drive] = []
    for line in out.splitlines():
        parts = line.split(None, 5)
        if len(parts) < 6:
            continue
        name, size_s, model, rota, tran, typ = (
            parts[0], parts[1], parts[2], parts[3], parts[4], parts[5]
        )
        if typ.lower() not in ("disk",):
            continue
        try:
            size_b: Optional[int] = int(size_s)
        except ValueError:
            size_b = None
        if tran.lower() == "nvme":
            kind = "NVMe SSD"
        elif rota == "0":
            kind = "SSD"
        elif rota == "1":
            kind = "HDD"
        else:
            kind = tran or "—"
        drives.append(Drive(name=f"/dev/{name}", size=size_b,
                            model=model.strip() or None, kind=kind))
    return drives


def collect_drives_macos() -> List[Drive]:
    if not which("diskutil"):
        return []
    rc, out, _ = run(["diskutil", "list"])
    if rc != 0:
        return []
    drives: List[Drive] = []
    # Find each physical `/dev/diskN` header line, then query it.
    for line in out.splitlines():
        m = re.match(r"^(/dev/disk\d+)\s*\(([^)]*)\):", line)
        if not m:
            continue
        dev, desc = m.group(1), m.group(2)
        if "synthesized" in desc.lower():
            # APFS synthesized containers — skip, they're not hardware.
            continue
        rc2, info, _ = run(["diskutil", "info", dev])
        if rc2 != 0:
            continue
        model = None
        size_b: Optional[int] = None
        kind = "—"
        for info_line in info.splitlines():
            il = info_line.strip()
            if il.startswith("Device / Media Name:"):
                model = il.split(":", 1)[1].strip()
            elif il.startswith("Disk Size:"):
                mm = re.search(r"\(([0-9]+) Bytes\)", il)
                if mm:
                    size_b = int(mm.group(1))
            elif il.startswith("Solid State:"):
                val = il.split(":", 1)[1].strip().lower()
                if val == "yes":
                    kind = "SSD"
                elif val == "no":
                    kind = "HDD"
            elif il.startswith("Protocol:"):
                proto = il.split(":", 1)[1].strip()
                if "NVMe" in proto and kind in ("SSD", "—"):
                    kind = "NVMe SSD"
        drives.append(Drive(name=dev, size=size_b, model=model, kind=kind,
                            extra=desc))
    return drives


def collect_drives_freebsd() -> List[Drive]:
    if not which("geom"):
        # Fall back to sysctl.
        drives: List[Drive] = []
        rc, out, _ = run(["sysctl", "-n", "kern.disks"])
        if rc != 0:
            return []
        for name in out.split():
            size_b: Optional[int] = None
            rc2, out2, _ = run(["diskinfo", name])
            if rc2 == 0 and out2:
                parts = out2.split()
                if len(parts) >= 3:
                    try:
                        size_b = int(parts[2])
                    except ValueError:
                        size_b = None
            drives.append(Drive(name=f"/dev/{name}", size=size_b))
        return drives

    rc, out, _ = run(["geom", "disk", "list"])
    if rc != 0:
        return []
    drives = []
    cur: Optional[Drive] = None
    for raw in out.splitlines():
        line = raw.rstrip()
        m = re.match(r"^Geom name:\s*(\S+)", line)
        if m:
            if cur is not None:
                drives.append(cur)
            cur = Drive(name=f"/dev/{m.group(1)}")
            continue
        if cur is None:
            continue
        s = line.strip()
        if s.startswith("Mediasize:"):
            mm = re.search(r"([0-9]+)\s*\(", s)
            if mm:
                cur.size = int(mm.group(1))
        elif s.startswith("descr:"):
            cur.model = s.split(":", 1)[1].strip()
        elif s.startswith("rotationrate:"):
            val = s.split(":", 1)[1].strip().lower()
            if val in ("0", "solid-state", "non-rotating"):
                cur.kind = "SSD"
            elif val.isdigit() and int(val) > 0:
                cur.kind = "HDD"
    if cur is not None:
        drives.append(cur)
    return drives


def collect_drives() -> List[Drive]:
    sysname = platform.system()
    if sysname == "Linux":
        return collect_drives_linux()
    if sysname == "Darwin":
        return collect_drives_macos()
    if sysname == "FreeBSD":
        return collect_drives_freebsd()
    return []


