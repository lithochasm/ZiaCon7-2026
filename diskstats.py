def _terminal_supports_color() -> bool:
    if not sys.stdout.isatty():
        return False
    if os.environ.get("NO_COLOR"):
        return False
    if os.environ.get("TERM") == "dumb":
        return False
    return True


def parse_args(argv: Sequence[str]) -> argparse.Namespace:
    ap = argparse.ArgumentParser(
        prog="diskstats",
        description="Cross-platform disk & storage report (macOS / Linux / FreeBSD).",
    )
    g = ap.add_mutually_exclusive_group()
    g.add_argument("--color",    action="store_true",
                   help="force colorized output")
    g.add_argument("--no-color", action="store_true",
                   help="disable ANSI color codes")
    g.add_argument("--html",     action="store_true",
                   help="emit inline-styled HTML (for emails)")
    ap.add_argument("--width", type=int, default=78,
                    help="width of the banner (default: 78)")
    ap.add_argument("--version", action="version",
                    version=f"diskstats {SCRIPT_VERSION}")
    return ap.parse_args(argv)


def main(argv: Optional[Sequence[str]] = None) -> int:
    args = parse_args(list(argv) if argv is not None else sys.argv[1:])
    report = build_report()

    if args.html:
        print(render_html(report))
        return 0

    if args.color:
        use_color = True
    elif args.no_color:
        use_color = False
    else:
        use_color = _terminal_supports_color()

    pal = Palette(enabled=use_color)
    print(render_text(report, pal, width=max(40, args.width)))
    return 0


if __name__ == "__main__":
    sys.exit(main())
