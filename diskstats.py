# Visual thresholds for usage coloring.
USAGE_WARN = 75.0
USAGE_CRIT = 90.0


def _pct_color(pct: float, pal: Palette) -> str:
    if pct >= USAGE_CRIT:
        return pal.bad
    if pct >= USAGE_WARN:
        return pal.warn
    return pal.ok


def _health_color(health: str, pal: Palette) -> str:
    h = (health or "").upper()
    if h == "ONLINE":
        return pal.ok
    if h in ("DEGRADED", "OFFLINE", "REMOVED"):
        return pal.warn
    if h in ("FAULTED", "UNAVAIL"):
        return pal.bad
    return pal.subtle


def progress_bar(pct: float, width: int, pal: Palette) -> str:
    pct = max(0.0, min(100.0, pct))
    filled = int(round(width * pct / 100.0))
    empty = width - filled
    color = _pct_color(pct, pal)
    return (
        f"{color}{GLYPHS['bar_full'] * filled}{pal.reset}"
        f"{pal.subtle}{GLYPHS['bar_empty'] * empty}{pal.reset}"
    )


def banner(title: str, subtitle: str, width: int, pal: Palette) -> str:
    top    = GLYPHS["tl"] + GLYPHS["h"] * (width - 2) + GLYPHS["tr"]
    bottom = GLYPHS["bl"] + GLYPHS["h"] * (width - 2) + GLYPHS["br"]

    # Title line — centered, bold.
    inner = width - 2
    t_pad = max(0, (inner - _visible_len(title)) // 2)
    t_line = (
        GLYPHS["v"]
        + " " * t_pad
        + f"{pal.heading}{title}{pal.reset}"
        + " " * (inner - t_pad - _visible_len(title))
        + GLYPHS["v"]
    )
    # Subtitle line.
    s_pad = max(0, (inner - _visible_len(subtitle)) // 2)
    s_line = (
        GLYPHS["v"]
        + " " * s_pad
        + f"{pal.subtle}{subtitle}{pal.reset}"
        + " " * (inner - s_pad - _visible_len(subtitle))
        + GLYPHS["v"]
    )
    return "\n".join([
        f"{pal.accent}{top}{pal.reset}",
        f"{pal.accent}{t_line}{pal.reset}",
        f"{pal.accent}{s_line}{pal.reset}",
        f"{pal.accent}{bottom}{pal.reset}",
    ])


_ansi_re = re.compile(r"\x1b\[[0-9;]*m")

def _visible_len(s: str) -> int:
    return len(_ansi_re.sub("", s))


def _pad(s: str, width: int, align: str = "left") -> str:
    vis = _visible_len(s)
    if vis >= width:
        return s
    slack = width - vis
    if align == "right":
        return " " * slack + s
    if align == "center":
        left = slack // 2
        right = slack - left
        return " " * left + s + " " * right
    return s + " " * slack


def section_header(title: str, pal: Palette) -> str:
    return (
        f"\n{pal.accent}{GLYPHS['h'] * 3}{pal.reset} "
        f"{pal.heading}{title}{pal.reset} "
        f"{pal.accent}{GLYPHS['h'] * 3}{pal.reset}"
    )


def render_filesystems(fss: List[Filesystem], pal: Palette) -> str:
    if not fss:
        return f"{pal.subtle}  (no filesystems reported){pal.reset}"

    # Each filesystem is rendered as a two-line block so long mount paths
    # never push the stats columns out of alignment:
    #
    #   /very/long/path  (ext4)
    #     › /dev/nvme0n1p2
    #          500.0 GiB used · 1.5 TiB free of 2.0 TiB    76.2%  ████████████████░░░░
    #
    fstype_w = max(4, min(10, max(_visible_len(f.fstype) for f in fss)))
    indent   = "    "  # stats row indent

    lines: List[str] = []
    for i, fs in enumerate(fss):
        pct     = fs.percent
        pct_col = _pct_color(pct, pal)
        bar     = progress_bar(pct, 24, pal)
        pct_str = f"{pct_col}{pct:5.1f}%{pal.reset}"

        # Header line: mount + fstype badge.
        header = (
            f"  {pal.label}{fs.mount}{pal.reset}  "
            f"{pal.subtle}({fs.fstype.strip()}){pal.reset}"
        )
        lines.append(header)

        # Optional source line (skip noisy/empty values).
        if (fs.source and fs.source not in ("none", "-", "—")
                and not fs.source.startswith("/dev/loop")):
            lines.append(
                f"{indent}{pal.subtle}{GLYPHS['arrow']} {fs.source}{pal.reset}"
            )

        # Stats line — a human-readable sentence plus bar + percentage.
        summary = (
            f"{pal.value}{human_bytes(fs.used)}{pal.reset} used"
            f" {pal.subtle}·{pal.reset} "
            f"{pal.value}{human_bytes(fs.avail)}{pal.reset} free of "
            f"{pal.value}{human_bytes(fs.size)}{pal.reset}"
        )
        lines.append(f"{indent}{summary}")
        lines.append(f"{indent}{bar}  {pct_str}")

        # Blank line between entries (but not after the last one).
        if i < len(fss) - 1:
            lines.append("")

    # Keep fstype_w referenced so lint is happy if we want to reintroduce a
    # compact mode later; it's intentionally computed above.
    _ = fstype_w
    return "\n".join(lines)


def render_drives(drives: List[Drive], pal: Palette) -> str:
    if not drives:
        return f"{pal.subtle}  (no physical drives detected){pal.reset}"
    name_w  = max(8, max(_visible_len(d.name) for d in drives))
    kind_w  = max(4, max(_visible_len(d.kind or "—") for d in drives))

    lines = []
    lines.append(
        f"  {pal.label}{_pad('Device', name_w)}{pal.reset}  "
        f"{pal.label}{_pad('Kind', kind_w)}{pal.reset}  "
        f"{pal.label}{_pad('Size', 10, 'right')}{pal.reset}  "
        f"{pal.label}Model{pal.reset}"
    )
    for d in drives:
        lines.append(
            f"  {pal.value}{_pad(d.name, name_w)}{pal.reset}  "
            f"{pal.accent}{_pad(d.kind or '—', kind_w)}{pal.reset}  "
            f"{_pad(human_bytes(d.size), 10, 'right')}  "
            f"{pal.subtle}{d.model or '—'}{pal.reset}"
        )
    return "\n".join(lines)


def render_zpools(pools: List[ZPool], pal: Palette) -> str:
    if not pools:
        return f"{pal.subtle}  (no ZFS pools){pal.reset}"

    lines = []
    for p in pools:
        hc = _health_color(p.health, pal)
        icon = GLYPHS["check"] if p.health.upper() == "ONLINE" else GLYPHS["warn"]

        pct_val: Optional[float] = None
        if p.size and p.alloc is not None:
            pct_val = p.alloc / p.size * 100.0

        header = (
            f"  {pal.label}{p.name}{pal.reset}   "
            f"{hc}{icon} {p.health}{pal.reset}"
        )
        lines.append(header)

        def kv(k: str, v: str) -> str:
            return (f"    {pal.subtle}{GLYPHS['bullet']}{pal.reset} "
                    f"{pal.label}{_pad(k, 9)}{pal.reset} {pal.value}{v}{pal.reset}")

        lines.append(kv("size",  human_bytes(p.size)))
        lines.append(kv("alloc", human_bytes(p.alloc)))
        lines.append(kv("free",  human_bytes(p.free)))
        if p.frag:  lines.append(kv("frag",  p.frag))
        if p.dedup: lines.append(kv("dedup", p.dedup))
        if p.cap:   lines.append(kv("cap",   p.cap))

        if pct_val is not None:
            bar = progress_bar(pct_val, 28, pal)
            pct_str = f"{_pct_color(pct_val, pal)}{pct_val:5.1f}%{pal.reset}"
            lines.append(f"    {bar}  {pct_str}")

        if p.errors:
            lines.append(f"    {pal.subtle}{p.errors}{pal.reset}")
        lines.append("")
    return "\n".join(lines).rstrip()


def render_text(report: Report, pal: Palette, width: int = 78) -> str:
    title = f"  Storage Report — {report.hostname}  "
    sub   = f"  {report.system} {report.release}  "
    out: List[str] = []
    out.append(banner(title, sub, width, pal))
    out.append(section_header("Filesystems", pal))
    out.append(render_filesystems(report.filesystems, pal))
    out.append(section_header("Physical Drives", pal))
    out.append(render_drives(report.drives, pal))
    out.append(section_header("ZFS Pools", pal))
    out.append(render_zpools(report.zpools, pal))

    if report.notes:
        out.append(section_header("Notes", pal))
        for n in report.notes:
            out.append(f"  {pal.warn}{GLYPHS['warn']}{pal.reset} {n}")

    out.append("")
    out.append(f"{pal.subtle}  diskstats {SCRIPT_VERSION}  ·  "
               f"generated on {report.hostname}{pal.reset}")
    return "\n".join(out)


