# Pseudo / virtual filesystems we never want to show.
SKIP_FSTYPES = {
    "devfs", "devtmpfs", "tmpfs", "proc", "sysfs", "cgroup", "cgroup2",
    "pstore", "bpf", "mqueue", "debugfs", "tracefs", "securityfs",
    "hugetlbfs", "fusectl", "overlay", "squashfs", "autofs", "nsfs",
    "ramfs", "rpc_pipefs", "configfs", "binfmt_misc", "fuse.portal",
    "fuse.gvfsd-fuse", "fuse.snapfuse",
}

SKIP_MOUNT_PREFIXES = (
    "/dev", "/proc", "/sys", "/run", "/snap", "/var/lib/docker",
    "/var/snap", "/System/Volumes/Preboot", "/System/Volumes/VM",
    "/System/Volumes/xarts", "/System/Volumes/iSCPreboot",
    "/System/Volumes/Hardware", "/System/Volumes/Update",
)


def _keep_fs(fs: Filesystem) -> bool:
    if fs.fstype.lower() in SKIP_FSTYPES:
        return False
    if fs.size == 0:
        return False
    for pref in SKIP_MOUNT_PREFIXES:
        if fs.mount.startswith(pref) and fs.mount != "/":
            return False
    return True


def collect_filesystems() -> List[Filesystem]:
    """Use `df -k` (portable) plus `-T`/`-Y` when available to learn fstype."""
    rc, out, _ = run(["df", "-Pk"])
    if rc != 0:
        rc, out, _ = run(["df", "-k"])
    if rc != 0 or not out:
        return []

    fstype_by_mount: dict[str, str] = {}
    sysname = platform.system()
    if sysname == "Linux":
        rc2, out2, _ = run(["df", "-PkT"])
        if rc2 == 0 and out2:
            for line in out2.splitlines()[1:]:
                parts = line.split()
                if len(parts) >= 7:
                    fstype_by_mount[parts[-1]] = parts[1]
    elif sysname == "FreeBSD":
        rc2, out2, _ = run(["df", "-kT"])
        if rc2 == 0 and out2:
            for line in out2.splitlines()[1:]:
                parts = line.split()
                if len(parts) >= 7:
                    # On FreeBSD df -T: Filesystem Type 1K-blocks Used Avail Capacity Mounted-on
                    fstype_by_mount[parts[-1]] = parts[1]
    elif sysname == "Darwin":
        rc2, out2, _ = run(["df", "-PY"])
        if rc2 == 0 and out2:
            for line in out2.splitlines()[1:]:
                parts = line.split()
                if len(parts) >= 7:
                    fstype_by_mount[parts[-1]] = parts[1]

    results: List[Filesystem] = []
    for line in out.splitlines()[1:]:
        parts = line.split()
        if len(parts) < 6:
            continue
        source = parts[0]
        try:
            size_k = int(parts[1])
            used_k = int(parts[2])
            avail_k = int(parts[3])
        except ValueError:
            continue
        mount = parts[-1]
        fstype = fstype_by_mount.get(mount, "—")
        results.append(Filesystem(
            source=source,
            mount=mount,
            fstype=fstype,
            size=size_k * 1024,
            used=used_k * 1024,
            avail=avail_k * 1024,
        ))

    results = [fs for fs in results if _keep_fs(fs)]
    # Stable ordering: root first, then by mountpoint.
    results.sort(key=lambda f: (f.mount != "/", f.mount))
    return results


