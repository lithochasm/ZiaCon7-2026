def collect_zpools() -> List[ZPool]:
    if not which("zpool"):
        return []
    # -H: no header, tab-separated.  -p: parsable (bytes) for size/alloc/free.
    rc, out, _ = run([
        "zpool", "list", "-H", "-p",
        "-o", "name,size,alloc,free,frag,cap,dedup,health",
    ])
    if rc != 0 or not out.strip():
        return []

    pools: List[ZPool] = []
    for line in out.splitlines():
        parts = line.split("\t")
        if len(parts) < 8:
            parts = line.split()
        if len(parts) < 8:
            continue
        name, size_s, alloc_s, free_s, frag_s, cap_s, dedup_s, health = parts[:8]

        def _to_int(s: str) -> Optional[int]:
            try:
                return int(s)
            except ValueError:
                return parse_size_suffixed(s)

        pool = ZPool(
            name=name,
            size=_to_int(size_s),
            alloc=_to_int(alloc_s),
            free=_to_int(free_s),
            frag=frag_s if frag_s != "-" else None,
            cap=cap_s if cap_s != "-" else None,
            dedup=dedup_s if dedup_s != "-" else None,
            health=health or "UNKNOWN",
        )
        # Ask for a one-line error summary.
        rc2, status, _ = run(["zpool", "status", "-x", name])
        if rc2 == 0 and status.strip():
            pool.errors = status.strip().splitlines()[0]
        pools.append(pool)
    return pools


