def run(cmd: Sequence[str], timeout: int = 10) -> Tuple[int, str, str]:
    """Run a command, returning (returncode, stdout, stderr). Never raises."""
    try:
        p = subprocess.run(
            cmd,
            capture_output=True,
            text=True,
            timeout=timeout,
            check=False,
        )
        return p.returncode, p.stdout, p.stderr
    except FileNotFoundError:
        return 127, "", f"command not found: {cmd[0]}"
    except subprocess.TimeoutExpired:
        return 124, "", f"timeout: {' '.join(cmd)}"
    except Exception as exc:  # pragma: no cover — defensive
        return 1, "", f"{type(exc).__name__}: {exc}"


def which(name: str) -> bool:
    return shutil.which(name) is not None


def human_bytes(n: Optional[int]) -> str:
    """Format a byte count as a compact human string (e.g. '1.4 TiB')."""
    if n is None:
        return "—"
    if n < 0:
        return "—"
    units = ["B", "KiB", "MiB", "GiB", "TiB", "PiB", "EiB"]
    x = float(n)
    for u in units:
        if x < 1024.0 or u == units[-1]:
            if u == "B":
                return f"{int(x)} {u}"
            return f"{x:.1f} {u}"
        x /= 1024.0
    return f"{n} B"


def parse_size_suffixed(text: str) -> Optional[int]:
    """Parse strings like '1.8T', '931.5G', '512M' into bytes."""
    if not text or text in ("-", "—"):
        return None
    m = re.match(r"^\s*([0-9]+(?:\.[0-9]+)?)\s*([KMGTPEZYkmgtpezy]?)(?:i?B)?\s*$", text)
    if not m:
        return None
    num = float(m.group(1))
    suf = m.group(2).upper()
    mult = {
        "":  1,
        "K": 1024 ** 1,
        "M": 1024 ** 2,
        "G": 1024 ** 3,
        "T": 1024 ** 4,
        "P": 1024 ** 5,
        "E": 1024 ** 6,
    }.get(suf, 1)
    return int(num * mult)


